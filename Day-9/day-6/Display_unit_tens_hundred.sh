#!/bin/bash -x

echo "Enter the number and show in words:"
read number

case $number in
                  0) echo "one" ;;
                  1) echo "ten" ;;
                  2) echo "one hundred" ;;
                  3) echo "one thousand" ;;
                  4) echo "one lakh" ;;
                  *) echo "enter number in (1,10,100,1000,10000) "
esac










